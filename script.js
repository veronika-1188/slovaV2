        // Элементы DOM
        const chatMessages = document.getElementById('chatMessages');
        const userInput = document.getElementById('userInput');
        const sendButton = document.getElementById('sendButton');
        
        // Функция для получения текущего времени в формате ЧЧ:ММ
        function getCurrentTime() {
            const now = new Date();
            return `${now.getHours().toString().padStart(2, '0')}:${now.getMinutes().toString().padStart(2, '0')}`;
        }
        
        // Функция добавления сообщения пользователя
        function addUserMessage(text) {
            const messageElement = document.createElement('div');
            messageElement.className = 'message user-message';
            messageElement.innerHTML = `
                ${text}
                <div class="message-time">${getCurrentTime()}</div>
            `;
            chatMessages.appendChild(messageElement);
            chatMessages.scrollTop = chatMessages.scrollHeight;
        }
        
        // Функция добавления сообщения бота
        function addBotMessage(text) {
            const messageElement = document.createElement('div');
            messageElement.className = 'message bot-message';
            messageElement.innerHTML = `
                <strong>Сленговый словарь:</strong> ${text}
                <div class="message-time">${getCurrentTime()}</div>
            `;
            chatMessages.appendChild(messageElement);
            chatMessages.scrollTop = chatMessages.scrollHeight;
        }
        
        // Функция обработки отправки сообщения
        function processUserInput() {
            const userText = userInput.value.trim().toLowerCase();
            
            if (!userText) return;
            
            // Добавляем сообщение пользователя в чат
            addUserMessage(userInput.value);
            
            // Проверяем, есть ли слово в словаре
            if (slangDictionary.hasOwnProperty(userText)) {
                // Если слово найдено, показываем определение
                setTimeout(() => {
                    addBotMessage(`Конечно, вот его определение: ${slangDictionary[userText]}.`);
                }, 500);
            } else {
                // Если слово не найдено
                setTimeout(() => {
                    addBotMessage(`Такого слова я не знаю. Попробуй еще раз.`);
                }, 500);
            }
            
            // Очищаем поле ввода
            userInput.value = '';
            userInput.focus();
        }
        
        // Обработчики событий
        sendButton.addEventListener('click', processUserInput);
        
        userInput.addEventListener('keypress', function(event) {
            if (event.key === 'Enter') {
                processUserInput();
            }
        });
        
        // Пример автозаполнения при загрузке
